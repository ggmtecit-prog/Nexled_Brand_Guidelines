#!/usr/bin/env node

/**
 * NexLed COMPONENTS.md Builder
 * Extracts component data from nexled.css and HTML snippet blocks,
 * then generates a structured COMPONENTS.md reference file.
 *
 * Usage: node scripts/build-components-md.js
 * Output: COMPONENTS.md (in repo root)
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const CSS_PATH = path.join(ROOT, 'src', 'nexled.css');
const HTML_FILES = ['atoms.html', 'molecules.html', 'organisms.html'].map(f => path.join(ROOT, f));
const OUTPUT_PATH = path.join(ROOT, 'COMPONENTS.md');

/**
 * Extract component class selectors from nexled.css
 * Groups them by section comments (/* SECTION_NAME *​/)
 */
function extractCSSComponents(cssContent) {
    const components = {};
    let currentSection = 'Uncategorized';

    const lines = cssContent.split('\n');
    for (const line of lines) {
        // Detect section headers like /* BUTTONS */ or /* ACCORDION */
        const sectionMatch = line.match(/^\/\*\s*([A-Z][A-Z\s/&-]+)\s*\*\/\s*$/);
        if (sectionMatch) {
            currentSection = sectionMatch[1].trim();
            if (!components[currentSection]) {
                components[currentSection] = { classes: new Set(), variants: new Set(), sizes: new Set(), states: new Set() };
            }
            continue;
        }

        // Extract class selectors
        const classMatch = line.match(/^\.([\w-]+)/);
        if (classMatch) {
            const className = classMatch[1];
            if (!components[currentSection]) {
                components[currentSection] = { classes: new Set(), variants: new Set(), sizes: new Set(), states: new Set() };
            }
            components[currentSection].classes.add(className);
        }

        // Detect state patterns
        if (line.includes('.is-open')) components[currentSection]?.states?.add('.is-open');
        if (line.includes('.is-active')) components[currentSection]?.states?.add('.is-active');
        if (line.includes('.is-selected')) components[currentSection]?.states?.add('.is-selected');
        if (line.includes('.is-disabled')) components[currentSection]?.states?.add('.is-disabled');
        if (line.includes('aria-expanded')) components[currentSection]?.states?.add('[aria-expanded]');
        if (line.includes('aria-selected')) components[currentSection]?.states?.add('[aria-selected]');
        if (line.includes('aria-pressed')) components[currentSection]?.states?.add('[aria-pressed]');
        if (line.includes('aria-disabled')) components[currentSection]?.states?.add('[aria-disabled]');
        if (line.includes(':hover')) components[currentSection]?.states?.add(':hover');
        if (line.includes(':focus-visible')) components[currentSection]?.states?.add(':focus-visible');
        if (line.includes(':disabled')) components[currentSection]?.states?.add(':disabled');
    }

    return components;
}

/**
 * Extract HTML snippets from <pre><code id="snippet-*"> blocks
 */
function extractSnippets(htmlContent, fileName) {
    const snippets = {};
    const regex = /<pre[^>]*>\s*<code[^>]*id="(snippet-[^"]+)"[^>]*>([\s\S]*?)<\/code>\s*<\/pre>/gi;
    let match;

    while ((match = regex.exec(htmlContent)) !== null) {
        const id = match[1];
        // Decode HTML entities
        let code = match[2]
            .replace(/&lt;/g, '<')
            .replace(/&gt;/g, '>')
            .replace(/&amp;/g, '&')
            .replace(/&quot;/g, '"')
            .replace(/&#39;/g, "'")
            .trim();
        snippets[id] = { code, source: path.basename(fileName) };
    }

    return snippets;
}

function build() {
    console.log('Building COMPONENTS.md...');

    // Read CSS
    if (!fs.existsSync(CSS_PATH)) {
        console.error(`CSS file not found: ${CSS_PATH}`);
        process.exit(1);
    }
    const cssContent = fs.readFileSync(CSS_PATH, 'utf-8');
    const components = extractCSSComponents(cssContent);

    // Read HTML snippets
    const allSnippets = {};
    for (const htmlFile of HTML_FILES) {
        if (fs.existsSync(htmlFile)) {
            const htmlContent = fs.readFileSync(htmlFile, 'utf-8');
            Object.assign(allSnippets, extractSnippets(htmlContent, htmlFile));
        } else {
            console.warn(`HTML file not found (skipping): ${htmlFile}`);
        }
    }

    // Build output
    const lines = [];
    lines.push('# NexLed Components Reference');
    lines.push('');
    lines.push('> Auto-generated by `scripts/build-components-md.js`');
    lines.push(`> Generated: ${new Date().toISOString().split('T')[0]}`);
    lines.push('> Source files: `src/nexled.css`, `atoms.html`, `molecules.html`, `organisms.html`');
    lines.push('');
    lines.push('## How to Use');
    lines.push('');
    lines.push('Every component follows the pattern: `[component] [variant] [size]`');
    lines.push('');
    lines.push('```html');
    lines.push('<button class="btn btn-primary btn-md">Label</button>');
    lines.push('```');
    lines.push('');
    lines.push('---');
    lines.push('');

    // Source Index
    lines.push('## Source Index');
    lines.push('');
    lines.push('### CSS Sections');
    lines.push('');
    for (const section of Object.keys(components)) {
        const classCount = components[section].classes.size;
        lines.push(`- **${section}** (${classCount} classes)`);
    }
    lines.push('');

    if (Object.keys(allSnippets).length > 0) {
        lines.push('### HTML Snippets Found');
        lines.push('');
        for (const [id, data] of Object.entries(allSnippets)) {
            lines.push(`- \`${id}\` (${data.source})`);
        }
        lines.push('');
    }

    lines.push('---');
    lines.push('');

    // Component sections
    for (const [section, data] of Object.entries(components)) {
        lines.push(`## ${section}`);
        lines.push('');
        lines.push('### Classes');
        lines.push('');
        const sortedClasses = Array.from(data.classes).sort();
        for (const cls of sortedClasses) {
            lines.push(`- \`.${cls}\``);
        }
        lines.push('');

        if (data.states.size > 0) {
            lines.push('### States');
            lines.push('');
            for (const state of data.states) {
                lines.push(`- \`${state}\``);
            }
            lines.push('');
        }

        // Find related snippets
        const sectionLower = section.toLowerCase();
        const relatedSnippets = Object.entries(allSnippets).filter(([id]) =>
            id.toLowerCase().includes(sectionLower.split(/[\s/]/)[0])
        );

        if (relatedSnippets.length > 0) {
            lines.push('### Snippets');
            lines.push('');
            for (const [id, data] of relatedSnippets) {
                lines.push(`#### ${id} (${data.source})`);
                lines.push('');
                lines.push('```html');
                lines.push(data.code);
                lines.push('```');
                lines.push('');
            }
        }

        lines.push('---');
        lines.push('');
    }

    // Write output
    const output = lines.join('\n');
    fs.writeFileSync(OUTPUT_PATH, output, 'utf-8');
    console.log(`COMPONENTS.md written (${output.length} bytes, ${Object.keys(components).length} sections, ${Object.keys(allSnippets).length} snippets)`);
}

build();
